'''你可以理解为加上f格式化之后，{}中的内容会被执行，而不仅仅是被打印，除非你加上引号。'''
# python3.6加入
# 能够直接引用其它变量
number = 99
name = "Rose"
print(f"我女儿叫{name},她考了{number}分。")

# python3.8加入
# 能够计算等式结果
print(f"这个算式的结果为{6788 - 4456}")
# 进制转换
v1 = f"媳妇儿今年{17}岁"
print(v1)
v2 = f"媳妇儿今年{17:#b}岁"
print(v2)
v3 = f"媳妇儿今年{17:#o}岁"
print(v3)
v4 = f"媳妇儿今年{17:#x}岁"
print(v4)